CREATE TABLE IF NOT EXISTS Worksheet (
id INT(11) AUTO_INCREMENT NOT NULL,
name VARCHAR(255) NOT NULL,
userId INT(11) NOT NULL,
FOREIGN KEY (userId) REFERENCES Users(id)
	ON UPDATE CASCADE
	ON DELETE RESTRICT,
PRIMARY KEY (id)
) ENGINE=InnoDB CHARACTER SET=UTF8;

